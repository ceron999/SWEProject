
public class Book {
	public int bookIndex;
	public String bookName;
	public String bookAuthor;
	public int bookPublicationYear;
	
	public Book(int getIndex, String getName, String getAuthor, int getYear)
	{
		bookIndex = getIndex;
		bookName = getName;
		bookAuthor = getAuthor;
		bookPublicationYear = getYear;
	}
}
